/**
 * scan_local.cpp — Standalone CVE and CIS compliance scanner for manual testing.
 *
 * Exercises the same detection logic as the vuln_scan plugin without requiring
 * the full Yuzu agent build (no gRPC, no vcpkg, no agent core).
 *
 * Compile on macOS (with nlohmann for JSON rule loading):
 *   clang++ -std=c++23 \
 *     -I agents/plugins/vuln_scan/src \
 *     -I $(brew --prefix nlohmann-json)/include \
 *     -o scan_local scripts/scan_local.cpp
 *
 * Compile on macOS (without nlohmann, compiled-in rules only):
 *   clang++ -std=c++23 -DYUZU_NO_DYNAMIC_RULES \
 *     -I agents/plugins/vuln_scan/src \
 *     -o scan_local scripts/scan_local.cpp
 *
 * Compile on Linux (g++-13):
 *   g++-13 -std=c++23 \
 *     -I agents/plugins/vuln_scan/src \
 *     -o scan_local scripts/scan_local.cpp
 *
 * Run:
 *   ./scan_local              # CVE + CIS scan
 *   ./scan_local --cve        # CVE scan only
 *   ./scan_local --cis        # CIS checks only
 *   ./scan_local --inventory  # print installed apps and exit
 *
 * If content/cve_rules.json exists (generated by scripts/generate-cve-rules.py),
 * it is loaded automatically and merged with the compiled-in rules.
 */

